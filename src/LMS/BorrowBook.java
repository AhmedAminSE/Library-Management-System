    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (jTextField1.getText().isEmpty() || jTextField2.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "You must Enter the User's ID and ISBN/Title of the Book !", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                Librarian l = new Librarian();
                int id = Integer.parseInt(jTextField1.getText()), pos = l.SearchLibraryUser(id);
                if (pos == -1) {
                    JOptionPane.showMessageDialog(null, "Wrong User ID !", "Error", JOptionPane.ERROR_MESSAGE);
                    jTextField1.setText("");
                } else {
                    Main.Users.get(pos).BorrowBook(jTextField2.getText().toLowerCase(), Main.Books);
                }
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "User_ID field must contain digits only !", "Error", JOptionPane.ERROR_MESSAGE);
                jTextField1.setText("");
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

