    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        char[] pass = jPasswordField1.getPassword();
        if (jTextField1.getText().isEmpty() || pass.length == 0) {
            JOptionPane.showMessageDialog(null, "Please enter Username and Password !", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            String password = new String(pass);
            if (!login(jTextField1.getText(), password)) {
                JOptionPane.showMessageDialog(null, "Incorrect Username and Password !", "Login", JOptionPane.ERROR_MESSAGE);
            } else {
                LibrarianHome h = new LibrarianHome();
                this.dispose();
                h.setVisible(true);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

