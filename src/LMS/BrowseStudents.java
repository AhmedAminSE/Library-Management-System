    public BrowseStudents() {
        super("Browse Students");
        initComponents();
        int RowsUsed = 0;
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(Main.Users.size());
        jTable1.getColumnModel().getColumn(0).setPreferredWidth(1);
        jTable1.getColumnModel().getColumn(1).setPreferredWidth(10);
        jTable1.getColumnModel().getColumn(2).setPreferredWidth(10);
        jTable1.getColumnModel().getColumn(3).setPreferredWidth(1);
        jTable1.getColumnModel().getColumn(4).setPreferredWidth(100);
        jTable1.getColumnModel().getColumn(5).setPreferredWidth(5);
        jTable1.getColumnModel().getColumn(6).setPreferredWidth(150);
        jTable1.getColumnModel().getColumn(7).setPreferredWidth(130);
        jTable1.getColumnModel().getColumn(8).setPreferredWidth(1);
        int row = 0;
        int col;
        for (int i = 0; i < Main.Users.size(); i++) {
            if (Main.Users.get(i).getRole().equals("Student")) {
                col = 0;
                LibraryUser LU = Main.Users.get(i);
                model.setValueAt(LU.getID(), row, col++);
                model.setValueAt(LU.getFname(), row, col++);
                model.setValueAt(LU.getLname(), row, col++);
                model.setValueAt(LU.getGender(), row, col++);
                model.setValueAt(LU.getEmail(), row, col++);
                model.setValueAt(LU.getPhoneNumber(), row, col++);
                model.setValueAt(LU.getAddress(), row, col++);
                model.setValueAt(LU.getFaculty(), row, col++);
                model.setValueAt(LU.getYear(), row, col);
                row++;
                RowsUsed++;
            }
        }
        model.setRowCount(RowsUsed);
        this.addWindowListener(new java.awt.event.WindowAdapter() {
            @Override
            public void windowClosing(WindowEvent we) {
                try {
                    Main.WriteData();
                    System.exit(0);
                } catch (Exception e) {
                }
            }
        });
    }

